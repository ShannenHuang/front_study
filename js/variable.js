// 数值与字符串的转换
// 1.数值转字符串
var num = 123;
var str = num.toString();
alert(typeof num);
alert(typeof str);
// 2.数值转字符串
var num2 = 123+"";
alert(typeof num2);
// 3.字符串转数值
var str2 = '456';
var num3 = Number(str2);
alert(typeof num3);